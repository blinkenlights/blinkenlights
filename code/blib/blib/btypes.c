#include "config.h"

#include <glib-object.h>

#include "btypes.h"

GType
b_filename_get_type (void)
{
  static GType filename_type = 0;

  if (!filename_type)
    {
      static const GTypeInfo type_info = { 0, };

      filename_type = g_type_register_static (G_TYPE_STRING, "BFilename", 
                                              &type_info, 0);
    }
  
  return filename_type;
}
